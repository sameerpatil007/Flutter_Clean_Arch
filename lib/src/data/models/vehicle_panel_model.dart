import '../../domain/entities/vehicle_panel.dart';

class VehiclePanelModel extends VehiclePanel {
  const VehiclePanelModel({
    required String id,
    required String panelId,
    required String name,
    required double price,
    required String visualName,
    required int r,
    required int cy,
    required int cx,
    required String mapCoordinate,
    required String mapClickableCoordinate,
    required String vehicleType,
    required List<String> serviceList,
    required double oversizeCostForOne,
    required bool isQuadrantSupported,
    required int pricingMethod,
    required double pricePerFeet,
    required bool isPerFeet,
    required bool isPackage,
    required bool isActive,
  }) : super(
          id: id,
          panelId: panelId,
          name: name,
          price: price,
          visualName: visualName,
          r: r,
          cy: cy,
          cx: cx,
          mapCoordinate: mapCoordinate,
          mapClickableCoordinate: mapClickableCoordinate,
          vehicleType: vehicleType,
          serviceList: serviceList,
          oversizeCostForOne: oversizeCostForOne,
          isQuadrantSupported: isQuadrantSupported,
          pricingMethod: pricingMethod,
          pricePerFeet: pricePerFeet,
          isPerFeet: isPerFeet,
          isPackage: isPackage,
          isActive: isActive,
        );

  factory VehiclePanelModel.fromJson(Map<String, dynamic> json) {
    return VehiclePanelModel(
      id: json['id'],
      panelId: json['panelId'],
      name: json['name'],
      price: json['price'],
      visualName: json['visualName'],
      r: json['r'],
      cy: json['cy'],
      cx: json['cx'],
      mapCoordinate: json['mapCoordinate'],
      mapClickableCoordinate: json['mapClickableCoordinate'],
      vehicleType: json['vehicleType'],
      serviceList: json['serviceList'].toList().cast<String>(),
      oversizeCostForOne: json['oversizeCostForOne'],
      isQuadrantSupported: json['isQuadrantSupported'],
      pricingMethod: json['pricingMethod'],
      pricePerFeet: json['pricePerFeet'],
      isPerFeet: json['isPerFeet'],
      isPackage: json['isPackage'],
      isActive: json['isActive'],
    );
  }
}
